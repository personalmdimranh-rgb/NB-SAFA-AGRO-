async function seed() {
  try {
    console.log('Connecting to database...');
    await mongoose.connect(MONGODB_URI);
    console.log('Connected to MongoDB successfully.');

    // 1. Fetch all Categories to map their ObjectIds
    const categories = await Category.find();
    console.log(`Fetched ${categories.length} categories from DB.`);

    const categoryMap = {};
    categories.forEach(c => {
      categoryMap[c.slug] = c._id;
    });

    // 2. Clear existing products to prevent duplicates or SKU violations
    console.log('Clearing existing products from collection...');
    await Product.deleteMany({});
    console.log('Products collection cleared.');

    // 3. Prepare products for seeding
    const productsToInsert = rawProducts.map(p => {
      const categoryId = categoryMap[p.categorySlug];
      if (!categoryId) {
        throw new Error(`Category ObjectId not found for slug: ${p.categorySlug}`);
      }

      return {
        name: p.name,
        slug: p.slug,
        description: p.description,
        price: p.price,
        salePrice: p.salePrice || null,
        discountRate: p.discountRate || null,
        sku: p.sku,
        stock: p.stock,
        categories: [categoryId],
        tags: p.tags,
        images: p.images,
        attributes: [
          { key: "Brand", value: "RPL Market Premium" },
          { key: "Origin", value: "Imported" },
          { key: "Hygienic Grade", value: "Clinical / Organic" }
        ],
        isFeatured: p.isFeatured || false,
        isNewArrival: p.isNewArrival || false,
        isFlashSale: p.isFlashSale || false,
        isPublished: true,
        ratings: p.ratings,
        numReviews: p.numReviews,
        views: p.views,
        totalSales: Math.floor(Math.random() * 20) + 5,
      };
    });

    // 4. Seed all 50 products into the collection
    console.log(`Inserting ${productsToInsert.length} products...`);
    const result = await Product.insertMany(productsToInsert);
    console.log(`Seeding complete! Successfully seeded ${result.length} products.`);

    console.log('Database seeding finished successfully!');
    process.exit(0);
  } catch (error) {
    console.error('Error seeding database:', error);
    process.exit(1);
  }
}

seed();
